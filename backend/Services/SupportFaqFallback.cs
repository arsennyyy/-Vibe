namespace MyMvcBackend.Services;

/// <summary>Ответы без внешнего API, если Gemini недоступен (регион BY/RU).</summary>
public static class SupportFaqFallback
{
    public static string? TryAnswer(string userMessage)
    {
        var m = userMessage.ToLowerInvariant();

        if (ContainsAny(m, "возврат", "вернут", "отмен", "refund"))
            return "Возврат билета зависит от правил мероприятия (обычно за 24–72 ч до начала). "
                   + "Если концерт отменили — деньги возвращаются автоматически, придёт письмо и уведомление в профиле. "
                   + "Подробнее — раздел FAQ «Оплата и возврат» или кнопка «Связь с поддержкой».";

        if (ContainsAny(m, "qr", "код", "вход", "скан"))
            return "QR-код билета находится в профиле → «Мои билеты». Код обновляется каждые 10 минут для безопасности. "
                   + "Покажите актуальный QR на входе.";

        if (ContainsAny(m, "билет", "купить", "покуп", "заказ", "оплат"))
            return "Чтобы купить билет: откройте мероприятие → вкладка со схемой зала → выберите место → оформите заказ. "
                   + "После оплаты билет появится в профиле с QR-кодом.";

        if (ContainsAny(m, "место", "схем", "зал", "ряд"))
            return "На странице мероприятия откройте схему зала: зелёные места свободны, серые заняты. "
                   + "Можно увеличить масштаб кнопками +/−.";

        if (ContainsAny(m, "профил", "аккаунт", "парол", "регистр", "войти", "логин"))
            return "Профиль доступен после входа (иконка в шапке). Там можно изменить имя, пароль, уведомления и посмотреть билеты.";

        if (ContainsAny(m, "почт", "email", "e-mail", "mail", "написать", "контакт", "связ"))
            return "Написать нам можно на projectmanagementtoll@gmail.com — этот адрес указан на странице «Контакты». "
                   + "Или нажмите «Связь с поддержкой» в чате для переписки с оператором.";

        if (ContainsAny(m, "привет", "здрав", "добр", "hello", "hi"))
            return "Здравствуйте! Я помощник +Vibe. Спросите про покупку билета, QR-код, возврат или схему зала. "
                   + "Если нужен оператор — нажмите «Связь с поддержкой».";

        return DefaultOfflineReply();
    }

    public static string DefaultOfflineReply() =>
        "Сейчас ИИ недоступен: Google Gemini не работает из Беларуси/РФ. "
        + "Спросите про билет, возврат, QR или схему зала — отвечу из FAQ. "
        + "Для сложного вопроса нажмите «Связь с поддержкой».";

    private static bool ContainsAny(string text, params string[] words) =>
        words.Any(w => text.Contains(w, StringComparison.Ordinal));
}
